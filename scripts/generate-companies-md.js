import { writeFileSync } from "fs";
import { buildCompanyRegistry } from "../lib/companies.js";
import { paths } from "../lib/paths.js";

function platformLabel(platform) {
  if (!platform || platform === "generic") return "—";
  return platform;
}

function row(entry) {
  const url = entry.url ? `[${entry.url}](${entry.url})` : "—";
  const jobs = entry.jobs != null ? String(entry.jobs) : "—";
  return `| ${entry.name} | ${platformLabel(entry.platform)} | ${url} | ${jobs} |`;
}

function section(title, entries, intro) {
  if (!entries.length) return "";
  const lines = [
    `## ${title}`,
    "",
    intro,
    "",
    "| Company | Platform | Career page | Jobs (sample) |",
    "| --- | --- | --- | --- |",
    ...entries.map(row),
    "",
  ];
  return lines.join("\n");
}

function main() {
  const { tracked, bonus, checkedAt } = buildCompanyRegistry();

  const verified = tracked.filter((e) => e.status === "verified");
  const unverified = tracked.filter((e) => e.status === "unverified" || e.status === "broken");
  const unresolved = tracked.filter((e) => e.status === "unresolved");

  const validated = checkedAt
    ? new Date(checkedAt).toLocaleDateString("en-IN", { dateStyle: "medium" })
    : "not yet run";

  const md = [
    "# Company Career Pages",
    "",
    "Curated career page URLs for **172 Indian startups and VC portfolio companies** (Titan Capital, Nexus, Elevation, Peak XV, AUM Ventures) plus AI/startup additions.",
    "",
    "Use this list to browse jobs directly, or run this repo's scraper to match roles against your resume.",
    "",
    `- **Verified scrapeable:** ${verified.length}`,
    `- **Has URL (needs fix or JS-heavy site):** ${unverified.length}`,
    `- **Unresolved:** ${unresolved.length}`,
    `- **Last validation:** ${validated}`,
    "",
    "Raw data: [`career-pages.json`](career-pages.json) · Known ATS overrides: [`data/known-ats-urls.json`](data/known-ats-urls.json)",
    "",
    section(
      "Verified (scrapeable)",
      verified.sort((a, b) => a.name.localeCompare(b.name)),
      "These URLs returned job listings in the last validation run."
    ),
    section(
      "Has URL (unverified or broken)",
      unverified.sort((a, b) => a.name.localeCompare(b.name)),
      "URL is known but scraper returned 0 jobs — often a JS-heavy careers site or wrong ATS slug."
    ),
    section(
      "Unresolved",
      unresolved.sort((a, b) => a.name.localeCompare(b.name)),
      "No career page URL found yet. Contributions welcome — add to `data/known-ats-urls.json` and open a PR."
    ),
    section(
      "Bonus (not in main list)",
      bonus,
      "Additional companies with known career URLs from earlier curation."
    ),
    "---",
    "",
    "_Generated by `npm run generate-companies-md`. Re-run after updating career pages or validation._",
    "",
  ].join("\n");

  writeFileSync(paths.companiesMd, md);
  console.log(`Wrote ${paths.companiesMd}`);
  console.log(`  Verified: ${verified.length}, Unverified/broken: ${unverified.length}, Unresolved: ${unresolved.length}, Bonus: ${bonus.length}`);
}

main();
