import { writeFileSync } from "fs";
import { buildCareersRegistry } from "../lib/careers.js";
import { paths } from "../lib/paths.js";

function row(entry) {
  const url = `[${entry.url}](${entry.url})`;
  const jobs = entry.jobs != null ? String(entry.jobs) : "—";
  return `| ${entry.platform} | ${url} | ${entry.status} | ${jobs} |`;
}

function section(title, entries, intro) {
  if (!entries.length) return "";
  return [
    `## ${title}`,
    "",
    intro,
    "",
    "| Platform | Career page | Status | Jobs (sample) |",
    "| --- | --- | --- | --- |",
    ...entries.map(row),
    "",
  ].join("\n");
}

function main() {
  const registry = buildCareersRegistry();
  const ok = registry.filter((e) => e.status === "ok");
  const unverified = registry.filter((e) => e.status === "unverified");
  const broken = registry.filter((e) => e.status === "broken" || e.status === "empty");

  const md = [
    "# Career Pages",
    "",
    "Single source of truth: [`careers.json`](careers.json) — **82 curated career page URLs** for Indian startups and deep-tech companies.",
    "",
    "Add a URL to `careers.json` and run the scraper. No separate company registry.",
    "",
    `- **Working:** ${ok.length}`,
    `- **Unverified:** ${unverified.length}`,
    `- **Broken / empty:** ${broken.length}`,
    `- **Total:** ${registry.length}`,
    "",
    section(
      "Working",
      ok.sort((a, b) => a.url.localeCompare(b.url)),
      "Returned job listings in the last validation run."
    ),
    section(
      "Unverified",
      unverified.sort((a, b) => a.url.localeCompare(b.url)),
      "Not yet validated — run `npm run validate-careers`."
    ),
    section(
      "Broken / empty",
      broken.sort((a, b) => a.url.localeCompare(b.url)),
      "URL exists but scraper returned 0 jobs or errored."
    ),
    "---",
    "",
    "_Generated by `npm run generate-careers-md`._",
    "",
  ].join("\n");

  writeFileSync(paths.careersMd, md);
  console.log(`Wrote ${paths.careersMd} (${registry.length} pages)`);
}

main();
